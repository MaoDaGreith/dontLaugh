package main

import "fmt"

func main() {
	//short declaration operator (declare a variable and assign at the same time)
	x := 322
	fmt.Println(x)
	//assigning a new value to the variable
	x = 442
	fmt.Println(x)
	y := 123 + 33
	fmt.Println(y)
	// (123 + 33) is an expression
	// + and := and = are operators
	// 123 and 33 are the operands
	// line 12 is a statement as well as line 13 and 7 etc.
}
